        <h2>Informaci&oacute;n del Parque Nacional Iguaz&uacute;</h2>
        <p>Primeramente podr&aacute; realizar una breve visita al Centro de Interpretaci&oacute;n Ybir&aacute; Reta "Pa&iacute;s de los &Aacute;rboles" en idioma guaran&iacute;, que le permitir&aacute; introducirlos al ambiente natural que preserva el Parque Nacional, su flora y fauna, las culturas que aqu&iacute; habitaron y los esfuerzos de la instituci&oacute;n por conservarlos.</p>
        <h3><strong>Tren de la Selva:</strong></h3>
        <p>El Tren Ecol&oacute;gico de la Selva; impulsado a gas natural; se dise&ntilde;&oacute; y construy&oacute; para el desplazamiento de los turistas en el &Aacute;rea Cataratas del Parque Nacional Iguaz&uacute;.</p>
        <p>Es abierto para que el visitante tome contacto directo con el medio ambiente y viva la experiencia de un paseo en medio de la selva, percibiendo los aromas, las variaciones clim&aacute;ticas y los sonidos del monte.</p>
        <h3><strong><span>El Tren posee tres estaciones:</span></strong></h3>
        <ul>
            <li><strong><span>Estaci&oacute;n Central</span></strong><span>: Duraci&oacute;n 7 min.<br /> A trav&eacute;s de la cual se accede a los trenes que van a la Estaci&oacute;n Cataratas y a la Estaci&oacute;n Garganta. Dispone de sanitarios y un &aacute;rea comercial conformada por una cafeter&iacute;a y un kiosco.</span></li>
            <li style="margin: 0cm; margin-bottom: .0001pt;"><strong><span style="font-size: 11.0pt; font-family: 'Calibri','sans-serif';">Estaci&oacute;n Cataratas:</span></strong><span style="font-size: 11.0pt; font-family: 'Calibri','sans-serif';"> Duraci&oacute;n 10 min.<br /> En esta estaci&oacute;n comienzan y confluyen los senderos que comunican a los Circuitos Inferior, Superior y el Sendero Verde. Funciona como una suerte de centro de distribuci&oacute;n con una plazoleta, un &aacute;rea de descanso, servicios de comidas r&aacute;pidas y sanitarios.</span></li>
            <li style="line-height: 12.0pt; background: white;"><strong><span style="font-size: 11.0pt; font-family: 'Calibri','sans-serif';">Estaci&oacute;n Garganta del Diablo:</span></strong><span style="font-size: 11.0pt; font-family: 'Calibri','sans-serif';"> Duraci&oacute;n 15 min.<br /> Desde aqu&iacute; se accede al paseo Garganta del Diablo. Posee una plazoleta de distribuci&oacute;n con servicios sanitarios y un local de comidas r&aacute;pidas.</span></li>
        </ul>
        <h3><span>&nbsp;<strong>Paseo Superior</strong>:</span></h3>
        <p ><span>Consta de una caminata corta sin escaleras que le permitir&aacute; apreciar la belleza de los Saltos desde arriba, llegando hasta el mirador del Salto Mbigua para disfrutar de la vista al Salto San Mart&iacute;n, el Segundo en importancia.</span></p>
        <ul>
            <li><strong><span>Duraci&oacute;n aprox</span></strong><span>:45 Minutos /&nbsp;650 metros</span></li>           
        </ul>

        <h3><span>&nbsp;<strong>Paseo Inferior</strong>:</span></h3>
        <p>&nbsp;Un recorrido m&aacute;s largo que el anterior y con tramos de escalera para acceder a otros saltos como el Lanuse , Dos hermanas y el Alvar N&uacute;&ntilde;ez cabeza de Vaca y llegar al bac&oacute;n inferior del Salto Bosetti con una inigualable vista.</p>
        <ul>
            <li><strong><span>Duraci&oacute;n aprox</span></strong><span>: 1 hora&nbsp; y media / 1.400 metros.</span></li>           
        </ul>
        <h3><span>&nbsp;<strong>Garganta del Diablo</strong>:</span></h3>
        <p >Con el tren ecol&oacute;gico podr&aacute; acceder desde la estaci&oacute;n Cataratas al&nbsp; comienzo de las pasarelas sobre el delta superior del r&iacute;o Iguaz&uacute; para disfrutar de la mejor vista al Salto principal.&nbsp;</p>
        <ul>
            <li><strong><span>Duraci&oacute;n aprox</span></strong><span>: 1 hora / 1.100 metros.</span></li>           
        </ul>
        <h3><span>&nbsp;<strong>Isla San Martin</strong>:</span></h3>
        <p >&nbsp;200 metros de escaleras con tramos de diferentes niveles. Vista &uacute;nica del Salto San Martin.</p>
        <ul>
            <li><strong><span>Duraci&oacute;n aprox</span></strong><span>: 1 hora / 700 metros.</span></li>           
        </ul>
        <p style="line-height: 12.0pt; background: white;"><span style="font-size: 10pt;">** La visita a la Isla est&aacute; sujeta al nivel y/o caudal &nbsp;del R&iacute;o Iguaz&uacute; ***</span></p>
        <p>&nbsp;</p>
        <h3><strong><span >Sendero Macuco:</span></strong></h3>
        <p ><span >Es un sendero natural de 6 km que empieza en Estaci&oacute;n Central, en pleno contacto directo con la flora y fauna de la selva paranaense, donde nos regala muchas sorpresa, detenerse a sentir los aromas de las flores que crecen al costado del camino, observar el verde intenso de la selva desde un mirador hacia el R&iacute;o Iguaz&uacute;, encontrarse con un grupo de Monos Ca&iacute;, son solamente algunas de las posibilidad. Al final del sendero, el encuentro con el Salto Arrechea llena de magia paradis&iacute;aca el paseo.</span></p>
        <ul>
            <li><strong><span>Duraci&oacute;n aprox</span></strong><span>: 1 hora / 1.100 metros.</span></li>  
            <li><strong><span>Dificultad</span></strong><span>: Alta.</span></li>    
        </ul>
